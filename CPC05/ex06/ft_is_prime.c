int		ft_is_prime(int nb)
{
	int i;
	int k;

	k = 1;
	i = nb - 1;
	if (nb == 1 || nb <= 0)
		return (0);
	else
	{
		while (i > 1)
		{
			if (nb % i == 0)
				k = 0;
			i--;
		}
		if (k)
			return (1);
		else
			return (0);
	}
}
