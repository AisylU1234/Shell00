int		ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb >= 1)
	{
		while (a <= nb)
		{
			if (a * a == nb)
				return (a);
			a++;
		}
		return (0);
	}
	else
		return (0);
}
