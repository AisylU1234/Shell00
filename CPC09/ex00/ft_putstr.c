#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	sym;

	i = 0;
	while (1)
	{
		sym = *(str + i);
		if (sym == '\0')
		{
			break ;
		}
		write(1, &sym, 1);
		i = i + 1;
	}
}
