int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;

	i = 0;
	while (length > 0)
	{
		if (f(*tab) != 0)
		{
			i++;
		}
		tab++;
		length--;
	}
	return (i);
}
